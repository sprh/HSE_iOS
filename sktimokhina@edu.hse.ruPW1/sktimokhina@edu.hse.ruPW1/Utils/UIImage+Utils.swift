import UIKit

extension UIImage {
    // Static vars for icons.
    static var car: UIImage {
        return UIImage(named: "car.fill")!
    }

    static var carBordered: UIImage {
        return UIImage(named: "car")!
    }

    static var rectangleBordered: UIImage {
        return UIImage(named: "rectangle")!
    }

    static var rectangle: UIImage {
        return UIImage(named: "rectangleBordered")!
    }
}
